package aggregation_Address;

public class Address {

	public int houseNo;
	public String houseName;
	public String city;
	public String state;
	
	public Address(int houseNo,String hName, String city, String state)
	{
		this.houseNo = houseNo;
		houseName = hName;
		this.city = city;
		this.state = state;
	}
	

}